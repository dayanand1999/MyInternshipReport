package com.entity;

public class Record {
	private int tid;
	private int scid;
	private int rcid;
	private double amount;
	
	
	public Record() {
	}


	public int getTid() {
		return tid;
	}


	public void setTid(int tid) {
		this.tid = tid;
	}


	public int getScid() {
		return scid;
	}


	public void setScid(int scid) {
		this.scid = scid;
	}


	public int getRcid() {
		return rcid;
	}


	public void setRcid(int rcid) {
		this.rcid = rcid;
	}


	public double getAmount() {
		return amount;
	}


	public void setAmount(double amount) {
		this.amount = amount;
	}
	
	

}
